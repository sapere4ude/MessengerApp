    /// Checks if user exists for given email
    /// Parameters
    /// - `email`:              Target email to be checked
    /// - `completion`:   Async closure to return with result
    
    // completion이 클로저로 사용된 것이라 다른 함수들 사용한 이후 나중에 실행됨
    public func userExists(with email: String,
                           completion: @escaping ((Bool) -> Void)) {

        let safeEmail = DatabaseManager.safeEmail(emailAddress: email)
        
        // observeSingleEventOfType (데이터 읽기) -> 추가된 이벤트 콜백이 한 번 트리거된 후에 다시 트리거되지 않는다. 이 방법은 한 번 로드된 후 자주 변경되지 않거나 능동적으로 수신 대기할 필요가 없는 데이터에 유용하다.
        // escaping closure 이기 때문에 { snapshot in } 부분은 결과가 모두 들어온 뒤에 실행된다.
        
        database.child(safeEmail).observeSingleEvent(of: .value, with: { snapshot in
            guard snapshot.value as? [String: Any] != nil else {
                completion(false)
                return
            }

            completion(true)
        })

    }

    /// Inserts new user to database
    
    // 기본 쓰기 작업은 setValue 코드를 사용하여 지정된 참조에 데이터를 저장하고 해당 경로의 기존 데이터를 모두 바꾼다.
    // .setValue를 통해 추가해줄 수 있다.
    
    public func insertUser(with user: ChatAppUser, completion: @escaping (Bool) -> Void) {
        database.child(user.safeEmail).setValue([
            "first_name": user.firstName,
            "last_name": user.lastName
        ], withCompletionBlock: { [weak self] error, _ in // strongSelf, weak self 확실하게 파악 X -> 메모리 누수를 막기위한 조치

            guard let strongSelf = self else {
                return
            }

            guard error == nil else {
                print("failed ot write to database")
                completion(false)
                return
            }
            
            /*
             데이터의 형태 - users
             [
                [
                    "name":
                    "safe_email"
                ],
                [
                    "name":
                    "safe_email"
                ]
             ]
             */
            // users 아래에 newElement를 저장하는 작업. newElement는 name, email로 구성되어 있다.
            // snapshot : 하위 데이터를 포함하여 해당 위치의 모든 데이터를 포함하는 역할
            // 근데 왜 이 과정을 usersCollection, newCollection 으로 나눈건지 모르겠음
            

            strongSelf.database.child("users").observeSingleEvent(of: .value, with: { snapshot in
                if var usersCollection = snapshot.value as? [[String: String]] {
                    // append to user dictionary
                    let newElement = [
                        "name": user.firstName + " " + user.lastName,
                        "email": user.safeEmail
                    ]
                    usersCollection.append(newElement)

                    // setValue(쓰기 작업)으로 usersCollection을 처리한다. completion을 false, true 나눈 이유를 정확하겐 모르겠음

                    strongSelf.database.child("users").setValue(usersCollection, withCompletionBlock: { error, _ in
                        guard error == nil else {
                            completion(false)
                            return
                        }

                        completion(true)
                    })
                }
                else {
                    // create that array
                    let newCollection: [[String: String]] = [
                        [
                            "name": user.firstName + " " + user.lastName,
                            "email": user.safeEmail
                        ]
                    ]

                    strongSelf.database.child("users").setValue(newCollection, withCompletionBlock: { error, _ in
                        guard error == nil else {
                            completion(false)
                            return
                        }

                        completion(true)
                    })
                }
            })
        })
    }

